package Java_oop;

public class Automobile {

	private String color;
	private String make;
	private int gear;

	public String getColor() {
		return this.color;

	}

	public void setColor(String color) {
		this.color = color;

	}

	public int getGear() {
		return this.gear;

	}

	public void setGear(int gear) {
		this.gear = gear;

	}

	public String getMake() {
		return this.make;

	}

	public void setMake(String make) {
		this.make = make;

	}

}
