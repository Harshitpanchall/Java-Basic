package question;

public class swap {
	public static void main(String[] args) {
		
		int a = 10;
		int b = 130;
		int c = 20;

		System.out.println("a" + a);
		System.out.println(b);
		System.out.println(c);
		
		///swap///
		
		a = 20;
		b= 10;
		c = 40;
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
	}

}
