package MultiLevelInheritance;

public class Bank {
	
protected double rate;
protected String name;

public double getRate() {
	return rate;

}
public void setRate(double rate) {
	this.rate = rate;
	
}
public String getName() {
	return name;
	
}

public void setName(String name) {
	this.name = name;
	
}

}
