package Overriding;

public class Shape {
	String color;
	int withborder;
	
	public String getColor() {
		return color;
		
	}
	
	public void setColor(String color) {
		this.color = color;
		
	}
	
	public int getWithborder() {
		return withborder;
	}
	
	public void setWithborder(int withborder) {
		this.withborder = withborder;
		
	}

}
